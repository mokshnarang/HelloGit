package com.niit.beans;

public class SimpleBean {

	private String name=null;
	private int age=0;
	
		
	public SimpleBean() {
		super();
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
